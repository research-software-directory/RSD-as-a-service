/**
 * Verifies if a URL points to a valid browser-renderable image.
 */
export default function isRealImage(url: string): Promise<boolean> {
  return new Promise((resolve) => {
    const img = new Image()
    img.onload = () => resolve(true)
    img.onerror = () => resolve(false)
    img.src = url
  })
}
